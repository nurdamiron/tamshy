async function main() {
  console.log('Seeding database...');

  // Create users
  const users = [];
  for (let i = 0; i < names.length; i++) {
    const user = await prisma.user.upsert({
      where: { phone: `+7700${String(i).padStart(7, '0')}` },
      update: { name: names[i] },
      create: {
        phone: `+7700${String(i).padStart(7, '0')}`,
        name: names[i],
        role: 'STUDENT',
      },
    });
    users.push(user);
  }

  // Create jury user
  const jury = await prisma.user.upsert({
    where: { phone: '+77001111111' },
    update: { name: 'Эксперт ИАЦ', role: 'JURY' },
    create: {
      phone: '+77001111111',
      name: 'Эксперт ИАЦ',
      role: 'JURY',
    },
  });

  // Create admin user
  await prisma.user.upsert({
    where: { phone: '+77009999999' },
    update: { name: 'Администратор', role: 'ADMIN' },
    create: {
      phone: '+77009999999',
      name: 'Администратор',
      role: 'ADMIN',
    },
  });

  console.log(`Created ${users.length} students + jury + admin`);

  // Create projects
  const projects = [];
  for (let i = 0; i < projectData.length; i++) {
    const pd = projectData[i];
    const user = users[i % users.length];
    const region = regions[i % regions.length];
    const school = `${schools[i % schools.length]} ${region === 'ASTANA' ? 'Астаны' : ''}`.trim();

    const project = await prisma.project.create({
      data: {
        title: pd.title,
        description: pd.desc,
        type: pd.type,
        status: i < 3 ? 'WINNER' : 'APPROVED',
        authorId: user.id,
        schoolName: school,
        region: region,
        teacherName: teachers[i % teachers.length],
        grade: 7 + (i % 5),
        juryScore: i < 3 ? 8 + (i % 3) : null,
        juryComment: i < 3 ? 'Отличный проект с практическим применением. Рекомендуется к масштабированию.' : null,
      },
    });
    projects.push(project);
  }

  console.log(`Created ${projects.length} projects`);

  // Create votes
  let voteCount = 0;
  for (const project of projects) {
    const numVotes = Math.floor(Math.random() * 8) + 3;
    const voters = users.sort(() => Math.random() - 0.5).slice(0, numVotes);

    for (const voter of voters) {
      if (voter.id === project.authorId) continue;
      try {
        await prisma.vote.create({
          data: { projectId: project.id, userId: voter.id },
        });
        voteCount++;
      } catch {
        // skip duplicates
      }
    }
  }

  console.log(`Created ${voteCount} votes`);
  console.log('Seed complete!');
  console.log('');
  console.log('Demo accounts:');
  console.log('  Student: +77000000000 (OTP: 000000 in dev mode)');
  console.log('  Jury:    +77001111111 (OTP: 000000 in dev mode)');
  console.log('  Admin:   +77009999999 (OTP: 000000 in dev mode)');
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
