// ── SEED ──────────────────────────────────────────────────────────────────
async function main() {
  console.log('🌱 Seeding database...');

  // 1. Учителя
  const teacherUsers = [];
  for (const t of TEACHERS) {
    const user = await prisma.user.upsert({
      where: { phone: t.phone },
      update: { name: t.name, role: 'TEACHER', consentPd: true, consentPdAt: new Date('2026-01-10'), consentSms: true, consentSmsAt: new Date('2026-01-10') },
      create: { phone: t.phone, name: t.name, role: 'TEACHER', consentPd: true, consentPdAt: new Date('2026-01-10'), consentSms: true, consentSmsAt: new Date('2026-01-10') },
    });
    teacherUsers.push({ ...user, region: t.region, school: t.school });
  }

  // Жюри и админ
  const jury = await prisma.user.upsert({
    where: { phone: '+77001111111' },
    update: { name: 'Ермаханов Полат Ермаханович', role: 'JURY' },
    create: { phone: '+77001111111', name: 'Ермаханов Полат Ермаханович', role: 'JURY' },
  });
  const jury2 = await prisma.user.upsert({
    where: { phone: '+77001111112' },
    update: { name: 'Момышев Талгат Дюсенович', role: 'JURY' },
    create: { phone: '+77001111112', name: 'Момышев Талгат Дюсенович', role: 'JURY' },
  });
  await prisma.user.upsert({
    where: { phone: '+77009999999' },
    update: { name: 'Администратор Tamshy', role: 'ADMIN' },
    create: { phone: '+77009999999', name: 'Администратор Tamshy', role: 'ADMIN' },
  });
  console.log(`  ✓ ${teacherUsers.length} teachers + 2 jury + admin`);

  // 2. Проекты + голоса
  await prisma.vote.deleteMany({});
  await prisma.project.deleteMany({});

  const allVoters = [jury, jury2, ...teacherUsers];
  const createdProjects = [];

  for (let i = 0; i < PROJECTS.length; i++) {
    const p = PROJECTS[i];
    const teacher = teacherUsers[p.teacherIdx % teacherUsers.length];
    const project = await prisma.project.create({
      data: {
        title: p.title,
        description: p.desc,
        type: p.type,
        status: p.status,
        fileUrl: p.fileUrl ?? null,
        videoUrl: ('videoUrl' in p ? p.videoUrl : null) ?? null,
        thumbnailUrl: p.img,
        authorId: teacher.id,
        schoolName: teacher.school,
        region: teacher.region as never,
        teacherName: teacher.name ?? '',
        studentName: p.student,
        grade: p.grade,
        juryScore: ('score' in p ? p.score : null) ?? null,
        juryComment: ('comment' in p ? p.comment : null) ?? null,
        createdAt: p.date,
      },
    });
    createdProjects.push({ ...project, targetVotes: p.votes });
  }

  // Голоса
  for (const proj of createdProjects) {
    if (proj.targetVotes === 0) continue;
    const shuffled = [...allVoters].sort(() => Math.random() - 0.5);
    const voters = shuffled.slice(0, Math.min(proj.targetVotes, shuffled.length));
    for (const voter of voters) {
      if (voter.id === proj.authorId) continue;
      try { await prisma.vote.create({ data: { projectId: proj.id, userId: voter.id } }); } catch { /* skip */ }
    }
  }
  console.log(`  ✓ ${createdProjects.length} projects (4 winners, 16 approved, 6 pending) + votes`);

  // 3. Конкурсы
  await prisma.contestDocument.deleteMany({});
  await prisma.contestSubmission.deleteMany({});
  await prisma.contest.deleteMany({});

  for (let ci = 0; ci < CONTESTS.length; ci++) {
    const c = CONTESTS[ci];
    const contest = await prisma.contest.create({
      data: {
        title: c.title, type: c.type, description: c.desc,
        rules: c.rules, status: c.status, deadline: c.deadline,
        imageUrl: c.img,
        documents: {
          create: c.docs.map(d => ({
            title: d.title, fileUrl: DUMMY_PDF, fileSize: d.size, fileType: 'PDF',
          })),
        },
      },
    });
    for (const sub of c.subs) {
      await prisma.contestSubmission.create({
        data: {
          contestId: contest.id,
          fullName: sub.name,
          birthDate: '2009-05-15',
          email: `${sub.name.toLowerCase().replace(/\s/g, '.')}@school.kz`,
          phone: `+77011${String(Math.floor(Math.random() * 9000000) + 1000000)}`,
          institution: sub.inst,
          region: sub.region as never,
          fileUrl: DUMMY_PDF,
          status: sub.status,
        },
      });
    }
  }
  console.log(`  ✓ ${CONTESTS.length} contests with submissions`);

  // 4. Материалы
  await prisma.material.deleteMany({});
  for (const m of MATERIALS) {
    await prisma.material.create({
      data: {
        title: m.title, description: m.desc,
        format: m.format, fileUrl: m.format === 'MP4' ? DUMMY_VIDEO : DUMMY_PDF,
        fileSize: m.size, type: m.type, audience: m.audience,
        year: m.year, downloads: m.downloads,
        views: ('views' in m ? m.views : 0) as number,
        featured: ('featured' in m && m.featured === true),
        imageUrl: m.img,
      },
    });
  }
  console.log(`  ✓ ${MATERIALS.length} educational materials`);

  // 5. Новости
  await prisma.news.deleteMany({});
  for (const n of NEWS) {
    await prisma.news.create({
      data: {
        title: n.title, content: n.content, category: n.cat,
        viewCount: n.views, createdAt: n.date, imageUrl: n.img,
        fileUrl: ('fileUrl' in n ? n.fileUrl : null) ?? null,
        photoCount: ('photoCount' in n ? n.photoCount : null) ?? null,
      },
    });
  }
  console.log(`  ✓ ${NEWS.length} news items (Jan–Apr 2026)`);

  // 6. Подписчики
  await prisma.newsletterSub.deleteMany({});
  const emails = ['teacher1@school.kz', 'eco.club@nis.edu.kz', 'zavuch@gymnasium91.kz', 'biology@school78.kz', 'green.school@mail.ru'];
  for (const email of emails) {
    await prisma.newsletterSub.upsert({ where: { email }, update: {}, create: { email } });
  }

  // 7. Контактные сообщения
  await prisma.contactMessage.deleteMany({});
  const msgs = [
    { name: 'Гульмира Сейтжан', email: 'g.seitjan@school45.kz', topic: 'Технический вопрос', message: 'Здравствуйте! При загрузке файла проекта появляется ошибка "файл слишком большой", хотя размер файла 48 МБ. Как решить эту проблему?' },
    { name: 'Алибек Жумабеков', email: 'a.jumab@nish.kz', topic: 'Вопрос о конкурсе', message: 'Добрый день. Можно ли подать одну работу сразу в две номинации — «Исследование» и «Цифровые решения»? Наш проект сочетает элементы обоих направлений.' },
    { name: 'Директор СШ №34 Павлодар', email: 'director@school34pvl.kz', topic: 'Партнёрство', message: 'Наша школа хотела бы стать официальным партнёром Tamshy и открыть водный клуб. Какие документы нужно подготовить? Просим выслать информацию.' },
  ];
  for (const m of msgs) {
    await prisma.contactMessage.create({ data: m });
  }

  console.log('  ✓ Newsletter subs + contact messages');

  // 8. Справочник водных объектов (Qazsu integration)
  const { seedWaterObjects } = await import('./seed-water-objects');
  await seedWaterObjects(prisma);

  console.log('\n✅ Seed complete! База данных заполнена актуальными данными.');
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
